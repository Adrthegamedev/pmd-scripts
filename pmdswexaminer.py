def wavi(f):
    newstream = bytearray()
    unk1 = bytearray(f.read(2))
    version = bytearray(f.read(2))
    chunkbeg = bytearray(f.read(4))
    chunklen = bytearray(f.read(4))
    streamcount = 0

    version.reverse()
    chunklen.reverse()

    newstream.extend(unk1)
    newstream.extend(version)
    newstream.extend(chunkbeg)
    newstream.extend(chunklen)

    #print(nbwavislots)
    #print(int.from_bytes(nbwavislots, endianness))
    #print(15 - ((f.tell()+15) % 16))
    for i in range(int.from_bytes(nbwavislots, endianness)):
        pointr = bytearray(f.read(2))
        streamcount = streamcount + 2
        #print("wavi pointr", pointr)
        pointr.reverse()
        newstream.extend(pointr)
    for i in range(15 - ((f.tell()+15) % 16)):
        newstream.extend(f.read(1))
        streamcount = streamcount + 1
    
    with open(fileout, "ab") as writefile:
        writefile.write(newstream)
    writefile.close()

    for i in range(int((int.from_bytes(chunklen, endianness) - streamcount)/64)):
        sampleinfo(f)
