def examine(f):
    header(f)
    while True:
        newstream = bytearray()

        label = f.read(4)

        newstream.extend(label)

        with open(fileout, "ab") as writefile:
            writefile.write(newstream)
        writefile.close()
        #print(label)
        if label == b"wavi":
            wavi(f)
        elif label == b"prgi":
            prgi(f)
        elif label == b"kgrp":
            kgrp(f)
        elif label == b"pcmd":
            pcmd(f)
        elif label == b"eod\x20":
            eod(f)
        else:
            print("error weird label", label, "at", f.tell())
            exit()
